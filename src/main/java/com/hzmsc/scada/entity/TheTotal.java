package com.hzmsc.scada.entity;

public class TheTotal {

	//设备生产总量
	private int equipmentSum;
	//员工生产总量
	private int employeeSum;
	//总产量
	private int productionSum;
	public int getEquipmentSum() {
		return equipmentSum;
	}
	public void setEquipmentSum(int equipmentSum) {
		this.equipmentSum = equipmentSum;
	}
	public int getEmployeeSum() {
		return employeeSum;
	}
	public void setEmployeeSum(int employeeSum) {
		this.employeeSum = employeeSum;
	}
	public int getProductionSum() {
		return productionSum;
	}
	public void setProductionSum(int productionSum) {
		this.productionSum = productionSum;
	}
	
	
}
